#pragma once

#include <kernel/platform/x86-64/msr.h>

typedef struct arch_cpu {
  struct arch_cpu *self;
} arch_cpu_t;

static inline void hdr_arch_cpu_set(arch_cpu_t *cpu) {
  cpu->self = cpu;
  hdr_x86_64_msr_write(X86_64_MSR_GSBASE, (uint64_t)cpu);
}
